  ^'Two white spheres without texture are shown in the light of three positional lights of different color. This example shows the use of scaling and translation to create a scene.

Positional lights do not have a direction: they emit their light in all directions. Use spotlights when you need both a position and a direction. Positional lights must have an attenuation which defines the reduction of light intensity at increasing distances. It is possible to define an attenuation that has only a constant part. 

Note that the smaller sphere is less reflective than the larger one. The difference is in the shininess attribute of the materials.'